    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
 if (tfUser.getText().equals("") || tfPw.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu!");
        } else {
            String user, pw;
            user = tfUser.getText();
            pw = tfPw.getText();

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Simulasi_UTSPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            Bukuu y = em.find(Bukuu.class, user);
            if (y != null) {
                JOptionPane.showMessageDialog(null, "Username sudah ada, coba gunakan username lain");
                bersih();
            } else {
                Login_1 x = new Login_1();
                x.setUsername(user);
                x.setPassword(pw);
                em.persist(x);

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Akun sukses dibuat");

                bersih();
                login z = new login();
                z.setVisible(true);
                this.dispose();
            }
            em.close();
            emf.close();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

